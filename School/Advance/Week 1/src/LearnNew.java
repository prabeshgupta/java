public class LearnNew {
    public static void main(String[] anyNames) {
        // Instead of args we can use any name and pass value using console
        for (String anyName : anyNames) {
            System.out.print('\n' + anyName);
        }

        /*
         * On console, type
         * java LearnNew "hello" "buddy" "first" "turn" "000"
         */
    }
}
