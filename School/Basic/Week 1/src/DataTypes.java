/* DATA TYPES IN JAVA

int : -1, 0, 1
float : 2.335
double : 3.325454
char : 'A'
String : "Apple"

 */

public class DataTypes {
    public static void main(String[] args) {
        String name = "Bulb";
        int price = 200;
        float weight = 10f;
        double radius = 5;

        System.out.println("Name is " + name);
        System.out.println("Price is " + price);
        System.out.println("Weight is " + weight);
        System.out.println("Radius is " + radius);
    }
}
