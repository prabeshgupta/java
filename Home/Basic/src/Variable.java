/*VARIABLES IN JAVA

Data Types
int --> Intergers
float --> Decimal numbers ends with f
double --> Advance float
char --> Single character enclosed within ''
String --> Words enclosed within ""
Boolean --> Either true or false


Syntax
dataType variableName
dataType variableName = value

*/
public class Variable {
    public static void main(String[] args) {

        String name = "Prabesh Gupta";
        int age = 20;
        float height = 5.12f;
        double netWorth = 30.00000;
        char gender = 'M';
        boolean isHindu = true;

        System.out.println("My name is " + name);
        System.out.println("Age is " + age);
        System.out.println("Height is " + height);
        System.out.println("Networth is " + netWorth);
        System.out.println("Gender is " + gender);
        System.out.println("Is Hindu?\n" + isHindu);

    }

}
