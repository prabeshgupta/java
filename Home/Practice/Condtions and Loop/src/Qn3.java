//Write a Java program to check whether a number is positive, negative, or zero.

public class Qn3 {
    public static void main(String[] args) {
        int num = -10;
        if (num > 0) {
            System.out.println("Postive");
        } else if (num == 0) {
            System.out.println("Zero");
        } else {
            System.out.println("Negative");
        }
    }
}
