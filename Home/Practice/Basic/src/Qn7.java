//Write a program to swap two numbers.

public class Qn7 {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 6;
        int num3;

        num3 = num1;
        num1 = num2;
        num2 = num3;

        System.out.println("Number 1 is " + num1);
        System.out.println("Number 2 is " + num2);
    }
}
